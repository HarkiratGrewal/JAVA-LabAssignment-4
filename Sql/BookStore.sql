DROP DATABASE IF EXISTS CENTENNIAL_BOOKSTORE_DB;

CREATE DATABASE CENTENNIAL_BOOKSTORE_DB;
USE CENTENNIAL_BOOKSTORE_DB;

CREATE TABLE BOOKS(
 BOOKCODE INT PRIMARY KEY,
 TITLE VARCHAR(50) NOT NULL ,
 AUTHOR VARCHAR(25) NOT NULL,
 PRICE DECIMAL(7,2) ,
 TYPE VARCHAR(25),
 SUBJECT VARCHAR(25));
 
 CREATE TABLE SALES(
 BOOKCODE INT ,
 SALESDATE DATE,
 QUANTITY INT NOT NULL,
 PRICE DECIMAL(7,2),
 CONSTRAINT F_KEY FOREIGN KEY(BOOKCODE) REFERENCES BOOKS(BOOKCODE));
 
